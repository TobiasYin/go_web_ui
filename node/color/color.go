package color

import (
	"fmt"
)

type Color struct {
	R byte
	G byte
	B byte
}

func NewColor(r int, g int, b int) Color {
	return Color{
		byte(r),
		byte(g),
		byte(b),
	}
}

func (c Color) ToHex() string {
	return fmt.Sprintf("#%.2x%.2x%.2x", c.R, c.G, c.B)
}

